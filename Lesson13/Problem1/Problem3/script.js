function sum(num1, num2) {
  return num1 + num2;
}

function multiply(num1, num2) {
  return num1 * num2;
}

function subtruct(num1, num2) {
  return num1 - num2;
}

function divide(num1, num2) {
  return num1 / num2;
}

console.log(sum(2,3));
console.log(multiply(2,3));
console.log(subtruct(2,3));
console.log(divide(2,3));